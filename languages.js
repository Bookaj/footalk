// Footalk Language Configuration

function merge(prev, curr) { return { ...prev, ...curr }; }

function generateLevels(steps) {
    const levels = [{}];
    const totalSteps = steps.length;

    for (let i = 1; i <= 100; i++) {
        // Proportional scaling: Level 100 = 100% of characters
        const count = Math.ceil((i / 100) * totalSteps);

        // Merge the first 'count' steps
        let currentMap = {};
        for (let k = 0; k < count; k++) {
            if (steps[k]) {
                currentMap = merge(currentMap, steps[k]);
            }
        }
        levels.push(currentMap);
    }
    return levels;
}

